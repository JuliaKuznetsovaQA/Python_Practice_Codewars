"""Given a 2D array of some suspended blocks (represented as hashtags),
return another 2D array which shows the end result once gravity is switched on."""

def switch_gravity_(lst):
    transposed = list(map(list, zip(*lst)))
    for i, m in enumerate(transposed):
        transposed[i] = sorted(m)[::-1]
    returned = list(map(list, zip(*transposed)))
    for i in returned:
        print(i)


